#ejemplos
#especificando un valor
for n in range(10):
    c=n**2
    print(n,c)

#especificando dos valores
for n in range(1,6):
    c=n**3
    print(n,c)

#especificando tres valores
#se especifica en inicio, final y el paso
for n in range(8,0,-1):
    print(n,c)
